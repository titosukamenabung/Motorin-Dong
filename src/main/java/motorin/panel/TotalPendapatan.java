    private void loadDataPendapatan() {
        try {
            // 1. Ambil Tanggal
            java.util.Date date1 = (java.util.Date) spinnerMulai.getValue();
            java.util.Date date2 = (java.util.Date) spinnerSampai.getValue();
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String tgl1 = sdf.format(date1);
            String tgl2 = sdf.format(date2);

            // 2. Reset Tabel
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            // 3. Query Ambil Data
            java.sql.Connection K = com.motorin.db.koneksi.Go();
            java.sql.Statement S = K.createStatement();

            // Ambil semua transaksi di tanggal itu
            String sql = "SELECT * FROM transaksi " +
                         "WHERE tanggal BETWEEN '" + tgl1 + "' AND '" + tgl2 + "'";
            
            java.sql.ResultSet R = S.executeQuery(sql);

            double totalSemua = 0; // Variabel penampung total duit
            int no = 1;

            // 4. Looping Isi Tabel
            while (R.next()) {
                String tgl = R.getString("tanggal");
                String id = R.getString("id_transaksi");
                String nama = R.getString("nama_pelanggan"); // Sesuaikan nama kolom di DB kamu
                
                double nominal = R.getDouble("total_harga");
                totalSemua += nominal; // Tambahkan ke celengan total
                
                // Format Rupiah per baris
                String nominalStr = String.format("Rp %,.0f", nominal).replace(',', '.');

                Object[] data = {no++, tgl, id, nama, nominalStr};
                model.addRow(data);
            }
            
            // 5. UPDATE LABEL BESAR DI BAWAH (PENTING!)
            String totalStr = String.format("Rp %,.0f", totalSemua).replace(',', '.');
            lblTotalPendapatan.setText(totalStr);

        } catch (Exception e) {
            e.printStackTrace(); // Cek error di output
        }
    }

